/* Declare several variables by selecting for each one of them the most appropriate of the
   types byte, short, int, long and in order to assign them the 
   following values: 52130; -115; 4825932; 97; -10000; */


package primitiveTypesAndVariables_2;

public class Exercise1 {

	public static void main(String[] args) {
		byte byte1 = 97;
		byte byte2 = -115;
		short short1 = -10000;
		int int1 = 52130;
		long long1 = 4825932L;
		
		System.out.println(byte1 + " " + byte2 + " " + short1 + " " + int1 + " " + long1);
	}

}
