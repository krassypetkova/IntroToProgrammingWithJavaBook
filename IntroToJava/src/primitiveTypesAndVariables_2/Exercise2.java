/* Which of the following values can be assigned to variables of type float and type double: 
   34.567839023; 12.345; 8923.1234857; 3456.091 ? */


package primitiveTypesAndVariables_2;

public class Exercise2 {

	public static void main(String[] args) {
		float float1 = 12.345f;
		float float2 = 3456.091f;
		double double1 = 34.567839023;
		double double2 = 8923.1234857;
		
		
		System.out.println(float1 + " " + float2);
		System.out.println(double1 + " " + double2);
	}

}
